package Day4;

public class NumberDataTypeDemo {
    public static void main(String[] args) {

        //put 0 before to any number becomes octal
        //put 0x before to any number becomes hexadecimal
        //put 0b before to any number becomes binary
int i=0b10;
        System.out.println(i);
        int j=0x796;
        System.out.println(j);

        int x=077;
        System.out.println(x);


    }
}
