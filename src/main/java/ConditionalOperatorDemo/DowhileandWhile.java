package ConditionalOperatorDemo;



public class DowhileandWhile {
    public static void main(String[] args) {

        int a=1;
        while(a<5){
            System.out.println("Hello World");
            a++;
        }

        int b=5;

        do {
            System.out.println("Hello World1");
        }while(b<5);

        //for loop
        for(int i=1;i<5;i++)
        {
            System.out.println("Hello World2");
        }
    }

}
